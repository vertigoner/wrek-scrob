
# Simple track object
class track():


	def __init__(self, artist = None, album = None, title = None):

		self.artist = artist
		self.album = album
		self.title = title


	def getArtist():

		return artist

	def getAlbum():

		return album

	def getTitle():

		return title

	def setArtist(artist):

		self.artist = artist

	def setAlbum(album):

		self.album = album

	def setTitle(title):

		self.title = title